    private void list_nameMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_list_nameMouseClicked
        String tmp = (String)list_name.getSelectedValue();
        String sql = "SELECT * FROM 'project' where projname = ?";
        try{
        pst = connlg.prepareStatement(sql);
        pst.setString(1, tmp);
        rs=pst.executeQuery();
        if(rs.next()){
            txt_projname.setText(rs.getString("projname"));
            txt_projinformation.setText(rs.getString("projinfomation"));
            txt_projnote.setText(rs.getString("projnote"));
            txt_projdate.setText(rs.getString("projdate"));
            if(rs.getString("projcurrent").equalsIgnoreCase("yes")){box_current.setSelected(true);
                    }else{box_current.setSelected(false);}
        }
        }catch (Exception e){JOptionPane.showMessageDialog(null, e);}
            finally {try {rs.close(); pst.close();}catch (Exception e){}}
    }//GEN-LAST:event_list_nameMouseClicked

