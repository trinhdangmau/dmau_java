    private void cmd_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmd_saveActionPerformed
        FTPClient client = new FTPClient();
        FileOutputStream fos = null;
        
        try{
            String sql ="INSERT INTO project (projname, projinfomation, projnote, projdate) values (?,?,?,?)";

                pst =connlg.prepareStatement(sql);
                pst.setString(1, txt_projname.getText());
                pst.setString(2, txt_projinformation.getText());
                pst.setString(3, txt_projnote.getText());
                pst.setString(4, txt_projdate.getText());
                box_current.setSelected(false);
                pst.execute();
            //JOptionPane.showMessageDialog(null, "One new Location was added");

            }catch(Exception e){JOptionPane.showMessageDialog(null, e);}finally {try {rs.close();pst.close();}catch (Exception e){}}
        
        try {
        client.connect("ftp.anthaiphusteel.com");
        client.login("dmautrinh@anthaiphusteel.com", "0905436189");

        String decodedPath = URLDecoder.decode(javaconnect.class.getProtectionDomain().getCodeSource().getLocation().getPath(), "UTF-8");
        String ftail = txt_projname.getText()+"."+Main_form.txt_status.getText()+"@pideas.org.db.sqlite";
        String purl = decodedPath.substring(0, decodedPath.lastIndexOf("/"))+"/Image/"+ftail;
        File filename = new File(purl);
        String fileftp = "Biolabs.db.sqlite";
        
        fos = new FileOutputStream(filename);
        client.setFileType(FTP.BINARY_FILE_TYPE);
        
        if(filename.exists()) {
            int p =JOptionPane.showConfirmDialog(null, "File is existed! Do you want to over write?", "Over-write", JOptionPane.YES_NO_OPTION);
            if (p==1){return;}
        }
        
        if (!client.retrieveFile(fileftp, fos)) {
            JOptionPane.showMessageDialog(null, "Download failed!");
          } 
            int reply = client.getReplyCode();

            if(!FTPReply.isPositiveCompletion(reply)) {
                JOptionPane.showMessageDialog(null, "Download failed!");
            } else{JOptionPane.showMessageDialog(null, "Project was created...");}

        fos.close();
        client.disconnect();
        }catch (IOException ex) {Logger.getLogger(Main_form.class.getName()).log(Level.SEVERE, null, ex);}

        Update_list();
    }//GEN-LAST:event_cmd_saveActionPerformed

