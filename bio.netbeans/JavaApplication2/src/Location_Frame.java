    private void table_locationMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_locationMouseClicked
        try{
        int row=table_location.getSelectedRow();
        String Table_click=(table_location.getModel().getValueAt(row, 0).toString());
            
            String sql="SELECT *  FROM 'location' WHERE 'location'.locationid ='"+Table_click+"'   ";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            
            if(rs.next()){
                String locationid =rs.getString("locationid");
                txt_locationid.setText(locationid);
                String locationname =rs.getString("name");
                txt_locationname.setText(locationname); 
                String coord =rs.getString("coordinate");
                txt_coord.setText(coord); 
                String wards =rs.getString("wards");
                txt_wards.setText(wards); 
                String city =rs.getString("city");
                txt_city.setText(city); 
                String province =rs.getString("province");
                txt_province.setText(province); 
                String country =rs.getString("country");
                txt_country.setText(country);
                String note =rs.getString("note");
                txt_note.setText(note); 
            }
        }catch(Exception e){ JOptionPane.showMessageDialog(null, e);}
            finally { try { rs.close(); pst.close();}catch (Exception e){  } }
    }//GEN-LAST:event_table_locationMouseClicked

