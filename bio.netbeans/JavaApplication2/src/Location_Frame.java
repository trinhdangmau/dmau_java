    private void cmd_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmd_saveActionPerformed
        try{
            String sql ="INSERT INTO location (locationid, name, coordinate, wards, city, province, country, note) values (?,?,?,?,?,?,?,?)";

                pst =conn.prepareStatement(sql);
                pst.setString(1, txt_locationid.getText());
                pst.setString(2, txt_locationname.getText());
                pst.setString(3, txt_coord.getText());
                pst.setString(4, txt_wards.getText());
                pst.setString(5, txt_city.getText());
                pst.setString(6, txt_province.getText());
                pst.setString(7, txt_country.getText());
                pst.setString(8, txt_note.getText());
                pst.execute();

            JOptionPane.showMessageDialog(null, "One new Location was added");

            }catch(Exception e){JOptionPane.showMessageDialog(null, e);}
                    finally {try {rs.close();pst.close();}catch (Exception e){}}

        Update_table_location();
    }//GEN-LAST:event_cmd_saveActionPerformed

